# ---- Summary Statistics ----

SEVERITY_WEIGHTS: dict[str, float] = {
    "critical": 4.0,
    "high": 3.0,
    "medium": 2.0,
    "low": 1.0,
}


def get_feedback_summary() -> dict[str, Any]:
    """Return aggregate statistics about the synthetic ticket corpus."""
    total = len(SYNTHETIC_TICKETS)
    severity_dist: dict[str, int] = {"critical": 0, "high": 0, "medium": 0, "low": 0}
    category_dist: dict[str, int] = {}
    total_arr = 0

    for t in SYNTHETIC_TICKETS:
        severity_dist[t["severity"]] = severity_dist.get(t["severity"], 0) + 1
        category_dist[t["category"]] = category_dist.get(t["category"], 0) + 1
        total_arr += t["arr"]

    avg_arr = total_arr // total if total else 0
    weighted_severity = sum(
        SEVERITY_WEIGHTS.get(t["severity"], 1) for t in SYNTHETIC_TICKETS
    ) / total

    return {
        "total_tickets": total,
        "severity_distribution": severity_dist,
        "category_distribution": category_dist,
        "total_arr_at_risk": total_arr,
        "average_arr": avg_arr,
        "weighted_avg_severity": round(weighted_severity, 2),
        "top_categories": sorted(category_dist.items(), key=lambda x: x[1], reverse=True)[:5],
    }
